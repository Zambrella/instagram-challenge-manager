/// An enum representing the different possible subscription lengths.
enum SubscriptionLength {
  /// 1 week (7 days)
  oneWeek,

  /// 1 month (30 days)
  oneMonth,

  /// 2 months
  twoMonths,

  /// 3 months
  threeMonths,

  /// 6 months
  sixMonths,

  /// 1 year
  oneYear,

  /// Lifetime subscription
  lifetime,

  /// Unknown subscription length
  unknown,
}
